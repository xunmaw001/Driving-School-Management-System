<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            xueyuanzhanghao : false,
            xueyuanxingming : false,
            jiashizhengleixing : false,
            kemu : false,
            fenshu : false,
            shifouhege : false,
            beizhu : false,
            dengjishijian : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          xueyuanzhanghao: '',
          xueyuanxingming: '',
          jiashizhengleixing: '',
          kemu: '',
          fenshu: '',
          shifouhege: '',
          beizhu: '',
          dengjishijian: '',
        },
        xueyuanzhanghaoOptions: [],
        kemuOptions: [],
        shifouhegeOptions: [],
        rules: {
          xueyuanzhanghao: [
          ],
          xueyuanxingming: [
          ],
          jiashizhengleixing: [
          ],
          kemu: [
          ],
          fenshu: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          shifouhege: [
          ],
          beizhu: [
          ],
          dengjishijian: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.dengjishijian = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='xueyuanzhanghao'){
              this.ruleForm.xueyuanzhanghao = obj[o];
              this.ro.xueyuanzhanghao = true;
              continue;
            }
            if(o=='xueyuanxingming'){
              this.ruleForm.xueyuanxingming = obj[o];
              this.ro.xueyuanxingming = true;
              continue;
            }
            if(o=='jiashizhengleixing'){
              this.ruleForm.jiashizhengleixing = obj[o];
              this.ro.jiashizhengleixing = true;
              continue;
            }
            if(o=='kemu'){
              this.ruleForm.kemu = obj[o];
              this.ro.kemu = true;
              continue;
            }
            if(o=='fenshu'){
              this.ruleForm.fenshu = obj[o];
              this.ro.fenshu = true;
              continue;
            }
            if(o=='shifouhege'){
              this.ruleForm.shifouhege = obj[o];
              this.ro.shifouhege = true;
              continue;
            }
            if(o=='beizhu'){
              this.ruleForm.beizhu = obj[o];
              this.ro.beizhu = true;
              continue;
            }
            if(o=='dengjishijian'){
              this.ruleForm.dengjishijian = obj[o];
              this.ro.dengjishijian = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.$http.get('option/xueyuan/xueyuanzhanghao', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.xueyuanzhanghaoOptions = res.data.data;
          }
        });
        this.kemuOptions = "科目一,科目二,科目三,科目四".split(',')
        this.shifouhegeOptions = "合格,不合格".split(',')
      },
      // 下二随
      xueyuanzhanghaoChange () {
        this.$http.get('follow/xueyuan/xueyuanzhanghao?columnValue=' + this.ruleForm.xueyuanzhanghao, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.xueyuanxingming){
              this.ruleForm.xueyuanxingming = res.data.data.xueyuanxingming
            }
            if(res.data.data.jiashizhengleixing){
              this.ruleForm.jiashizhengleixing = res.data.data.jiashizhengleixing
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('xueyuanchengji/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('xueyuanchengji/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('xueyuanchengji/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('xueyuanchengji/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

