// 数字，邮件，手机，url，身份证校验
import { isNumber,isIntNumer,isEmail,isPhone, isMobile,isURL,checkIdCard } from "@/utils/validate";
export default {
	data() {
		let self = this
		var validateIdCard = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!checkIdCard(value)) {
				callback(new Error("请输入正确的身份证号码"));
			} else {
				callback();
			}
		};
		var validateUrl = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isURL(value)) {
				callback(new Error("请输入正确的URL地址"));
			} else {
				callback();
			}
		};
		var validateMobile = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isMobile(value)) {
				callback(new Error("请输入正确的手机号码"));
			} else {
				callback();
			}
		};
		var validatePhone = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isPhone(value)) {
				callback(new Error("请输入正确的电话号码"));
			} else {
				callback();
			}
		};
		var validateEmail = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isEmail(value)) {
				callback(new Error("请输入正确的邮箱地址"));
			} else {
				callback();
			}
		};
		var validateNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isNumber(value)) {
				callback(new Error("请输入数字"));
			} else {
				callback();
			}
		};
		var validateIntNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isIntNumer(value)) {
				callback(new Error("请输入整数"));
			} else {
				callback();
			}
		};
		return {
			id: '',
			type: '',
			
			
			ro:{
				biaoti : false,
				fengmiantu : false,
				biaoqian : false,
				jiashizhengleixing : false,
				kemu : false,
				shipin : false,
				zhishijieshao : false,
				faburiqi : false,
				clicktime : false,
				clicknum : false,
			},
			
			
			ruleForm: {
				biaoti: '',
				fengmiantu: '',
				biaoqian: '',
				jiashizhengleixing: '',
				kemu: '',
				shipin: '',
				zhishijieshao: '',
				faburiqi: '',
				clicktime: '',
			},
		
			kemuOptions: [],
			
			rules: {
				biaoti: [
					{ required: true, message: '标题不能为空', trigger: 'blur' },
				],
				fengmiantu: [
				],
				biaoqian: [
				],
				jiashizhengleixing: [
				],
				kemu: [
					{ required: true, message: '科目不能为空', trigger: 'blur' },
				],
				shipin: [
				],
				zhishijieshao: [
				],
				faburiqi: [
				],
				clicktime: [
				],
				clicknum: [
					{ validator: validateIntNumber, trigger: 'blur' },
				],
			}
		};
	},
	props: ["parent"],
	computed: {



	},
	created() {
	},
	methods: {
		
		// 下载
		download(file){
			window.open(`${file}`)
		},
		// 初始化
		init(id,type) {
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='biaoti'){
							this.ruleForm.biaoti = obj[o];
							this.ro.biaoti = true;
							continue;
						}
						if(o=='fengmiantu'){
							this.ruleForm.fengmiantu = obj[o];
							this.ro.fengmiantu = true;
							continue;
						}
						if(o=='biaoqian'){
							this.ruleForm.biaoqian = obj[o];
							this.ro.biaoqian = true;
							continue;
						}
						if(o=='jiashizhengleixing'){
							this.ruleForm.jiashizhengleixing = obj[o];
							this.ro.jiashizhengleixing = true;
							continue;
						}
						if(o=='kemu'){
							this.ruleForm.kemu = obj[o];
							this.ro.kemu = true;
							continue;
						}
						if(o=='shipin'){
							this.ruleForm.shipin = obj[o];
							this.ro.shipin = true;
							continue;
						}
						if(o=='zhishijieshao'){
							this.ruleForm.zhishijieshao = obj[o];
							this.ro.zhishijieshao = true;
							continue;
						}
						if(o=='faburiqi'){
							this.ruleForm.faburiqi = obj[o];
							this.ro.faburiqi = true;
							continue;
						}
						if(o=='clicktime'){
							this.ruleForm.clicktime = obj[o];
							this.ro.clicktime = true;
							continue;
						}
						if(o=='clicknum'){
							this.ruleForm.clicknum = obj[o];
							this.ro.clicknum = true;
							continue;
						}
				}
				










			}
			
			
			// 获取用户信息
			this.$http({
				url: `${this.$storage.get('sessionTable')}/session`,
				method: "get"
			}).then(({ data }) => {
				if (data && data.code === 0) {
					
					var json = data.data;
				} else {
					this.$message.error(data.msg);
				}
			});
			
            this.kemuOptions = "科目一,科目二,科目三,科目四".split(',')
			
		},
    // 多级联动参数

    info(id) {
      this.$http({
        url: `jiashizhishi/info/${id}`,
        method: "get"
      }).then(({ data }) => {
        if (data && data.code === 0) {
        this.ruleForm = data.data;
	//解决前台上传图片后台不显示的问题
	let reg=new RegExp('../../../upload','g')//g代表全部
	this.ruleForm.zhishijieshao = this.ruleForm.zhishijieshao.replace(reg,'../../../springboot7e8wi/upload');
        } else {
          this.$message.error(data.msg);
        }
      });
